import turtle

my_turtle = turtle.Turtle()

# Go to a certain cordinate
my_turtle.goto(50, 75)

my_turtle.forward(100)  # Move 100 pixels
my_turtle.left(90)  # Turn 90 degrees left
my_turtle.forward(200)
my_turtle.left(90)
my_turtle.forward(100)
my_turtle.left(90)
my_turtle.forward(200)

turtle.done()
